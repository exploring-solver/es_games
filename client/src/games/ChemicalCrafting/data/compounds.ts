export function getDailyCompound(): Compound {
  const date = new Date();
  const dayOfYear = Math.floor((date.getTime() - new Date(date.getFullYear(), 0, 0).getTime()) / 86400000);
  const compoundIds = Object.keys(COMPOUNDS);
  const compoundId = compoundIds[dayOfYear % compoundIds.length];
  return COMPOUNDS[compoundId];
}
