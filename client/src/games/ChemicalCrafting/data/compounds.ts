  molarMass: number;
  boilingPoint: number; // Kelvin
  meltingPoint: number; // Kelvin
  solubility: 'soluble' | 'insoluble' | 'slightly-soluble';
  reactivity: 'stable' | 'reactive' | 'highly-reactive';
  commonName?: string;
  uses?: string;
  hazards?: string[];
  reactionType?: 'exothermic' | 'endothermic' | 'neutral';
