export type ParticleType =
  | 'electron'
  | 'positron'
  | 'proton'
  | 'antiproton'
  | 'neutron'
  | 'photon'
  | 'upQuark'
  | 'downQuark'
  | 'charmQuark'
  | 'strangeQuark'
  | 'topQuark'
  | 'bottomQuark'
  | 'gluon'
  | 'wBoson'
  | 'zBoson'
  | 'higgs'
  | 'muon'
  | 'tau'
  | 'neutrino';

export interface ParticleData {
  id: ParticleType;
  name: string;
  symbol: string;
  mass: number; // in MeV/c²
  charge: number; // in elementary charges
  spin: number; // in units of ħ/2
  color: string;
  category: 'lepton' | 'quark' | 'boson' | 'baryon' | 'meson';
  lifetime: number; // in seconds (0 = stable)
  discoveryYear: number;
  rarity: number; // 1-10, 10 being rarest
  energyThreshold: number; // minimum collision energy to produce (GeV)
  description: string;
}

